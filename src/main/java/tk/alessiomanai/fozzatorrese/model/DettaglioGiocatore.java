package tk.alessiomanai.fozzatorrese.model;

public class DettaglioGiocatore extends Giocatore {

    private String dataNascita;
    private String luogoNascita;
    private String altezza;
    private String nazionalita;
    private String posizione;
    private String piede;
    private String inRosaDa;
    private String scadenzaContratto;
    private String ultimoRinnovo;
    private String causaAssenza;
    private String valoreDiMercato;

    public String getDataNascita() {
        return dataNascita;
    }

    public void setDataNascita(String dataNascita) {
        this.dataNascita = dataNascita;
    }

    public String getLuogoNascita() {
        return luogoNascita;
    }

    public void setLuogoNascita(String luogoNascita) {
        this.luogoNascita = luogoNascita;
    }

    public String getAltezza() {
        return altezza;
    }

    public void setAltezza(String altezza) {
        this.altezza = altezza;
    }

    public String getNazionalita() {
        return nazionalita;
    }

    public void setNazionalita(String nazionalita) {
        this.nazionalita = nazionalita;
    }

    public String getPosizione() {
        return posizione;
    }

    public void setPosizione(String posizione) {
        this.posizione = posizione;
    }

    public String getPiede() {
        return piede;
    }

    public void setPiede(String piede) {
        this.piede = piede;
    }

    public String getInRosaDa() {
        return inRosaDa;
    }

    public void setInRosaDa(String inRosaDa) {
        this.inRosaDa = inRosaDa;
    }

    public String getScadenzaContratto() {
        return scadenzaContratto;
    }

    public void setScadenzaContratto(String scadenzaContratto) {
        this.scadenzaContratto = scadenzaContratto;
    }

    public String getUltimoRinnovo() {
        return ultimoRinnovo;
    }

    public void setUltimoRinnovo(String ultimoRinnovo) {
        this.ultimoRinnovo = ultimoRinnovo;
    }

    public String getCausaAssenza() {
        return causaAssenza;
    }

    public void setCausaAssenza(String causaAssenza) {
        this.causaAssenza = causaAssenza;
    }

    public String getValoreDiMercato() {
        return valoreDiMercato;
    }

    public void setValoreDiMercato(String valoreDiMercato) {
        this.valoreDiMercato = valoreDiMercato;
    }
}

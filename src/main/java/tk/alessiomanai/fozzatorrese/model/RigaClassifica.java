package tk.alessiomanai.fozzatorrese.model;

public class RigaClassifica {

    private String posizione;
    private String nomeSquadra;
    private String punti;
    private String giocate;
    private String vinte;
    private String pareggiate;
    private String perse;
    private String golFatti;
    private String golSubiti;
    private String differenzaReti;
    private String stemma;

    public String getPosizione() {
        return posizione;
    }

    public void setPosizione(String posizione) {
        this.posizione = posizione;
    }

    public String getNomeSquadra() {
        return nomeSquadra;
    }

    public void setNomeSquadra(String nomeSquadra) {
        this.nomeSquadra = nomeSquadra;
    }

    public String getPunti() {
        return punti;
    }

    public void setPunti(String punti) {
        this.punti = punti;
    }

    public String getGiocate() {
        return giocate;
    }

    public void setGiocate(String giocate) {
        this.giocate = giocate;
    }

    public String getVinte() {
        return vinte;
    }

    public void setVinte(String vinte) {
        this.vinte = vinte;
    }

    public String getPareggiate() {
        return pareggiate;
    }

    public void setPareggiate(String pareggiate) {
        this.pareggiate = pareggiate;
    }

    public String getPerse() {
        return perse;
    }

    public void setPerse(String perse) {
        this.perse = perse;
    }

    public String getGolFatti() {
        return golFatti;
    }

    public void setGolFatti(String golFatti) {
        this.golFatti = golFatti;
    }

    public String getGolSubiti() {
        return golSubiti;
    }

    public void setGolSubiti(String golSubiti) {
        this.golSubiti = golSubiti;
    }

    public String getDifferenzaReti() {
        return differenzaReti;
    }

    public void setDifferenzaReti(String differenzaReti) {
        this.differenzaReti = differenzaReti;
    }

    public String getStemma() {
        return stemma;
    }

    public void setStemma(String stemma) {
        this.stemma = stemma;
    }
}
